"""
Multiplier class
"""


class Multiplier():
    """
    Multiplier class.
    Produces operand1 * operand2
    """
    @staticmethod
    def calc(operand_1, operand_2):
        """
        method to multiply two given values
        :param operand_1: first operand (a number)
        :param operand_2: second operand (a number)
        :return: returns the result of multiplying the two above parameters
        """
        return operand_1*operand_2
