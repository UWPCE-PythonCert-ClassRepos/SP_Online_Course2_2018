"""
Divider class
"""


class Divider():
    """
    Divder class
    Produces operand1/operand2
    """
    @staticmethod
    def calc(operand_1, operand_2):
        """
        method to divide operand1/operand2
        :param operand_1: a number
        :param operand_2: a number
        :return: the result of operand1/operand2
        """
        return operand_1/operand_2
