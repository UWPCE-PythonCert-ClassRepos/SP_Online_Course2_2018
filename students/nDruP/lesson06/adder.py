"""
This module accesses the Adder class.
"""


class Adder(object):
    """
    Adder contains 1 method that returns the sum of its inputs.
    """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Return the sum of operand_1 and operand_2
        """
        return operand_1 + operand_2
